def process_rental_request(raw_input: str, sender: str = "whatsapp_user") -> Dict[str, Any]:
    """
    Main function to process a rental agreement request through the agent pipeline
    
    Args:
        raw_input: The user's raw message with rental details
        sender: Identifier for the message sender
    
    Returns:
        Dictionary containing the final state and results
    """
    print("\n" + "="*60)
    print("🚀 STARTING AG ASSOCIATES AI WORKFLOW")
    print("="*60)
    
    # Initialize state
    initial_state = AgentState(
        raw_input=raw_input,
        sender=sender,
        tenant_name=None,
        landlord_name=None,
        rent_amount=None,
        property_address=None,
        agreement_start_date=None,
        agreement_duration=None,
        security_deposit=None,
        extracted_json=None,
        template_id=None,
        template_content=None,
        drafted_document=None,
        pdf_path=None,
        audit_passed=False,
        audit_feedback=None,
        revision_count=0,
        current_node="init",
        errors=[],
        timestamps={}
    )
    
    # Build and run the graph
    graph = build_agent_graph()
    
    try:
        final_state = graph.invoke(initial_state)
        
        print("\n" + "="*60)
        print("✅ WORKFLOW COMPLETED")
        print("="*60)
        
        # Summary
        print(f"\n📋 SUMMARY:")
        print(f"   Tenant: {final_state.get('tenant_name') or 'N/A'}")
        print(f"   Property: {(final_state.get('property_address') or 'N/A')[:50]}...")
        print(f"   Rent: {final_state.get('rent_amount') or 'N/A'}")
        print(f"   Document: {final_state.get('pdf_path', 'Not generated')}")
        print(f"   Audit: {'PASSED' if final_state.get('audit_passed') else 'FAILED'}")
        print(f"   Revisions: {final_state.get('revision_count', 0)}")
        
        return {
            "success": True,
            "state": final_state,
            "document_path": final_state.get('pdf_path'),
            "audit_passed": final_state.get('audit_passed'),
            "extracted_data": final_state.get('extracted_json')
        }
        
    except Exception as e:
        print(f"\n❌ WORKFLOW FAILED: {str(e)}")
        return {
            "success": False,
            "error": str(e),
            "state": initial_state
        }


