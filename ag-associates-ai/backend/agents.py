def should_revise(state: AgentState) -> str:
    """
    Conditional edge: Decide whether to revise or finish
    """
    if state['audit_passed']:
        print("✅ Audit passed - proceeding to finish")
        return "finish"
    elif state['revision_count'] > 3:
        print("⚠️ Max revisions reached - finishing anyway")
        return "finish"
    else:
        print(f"🔄 Audit failed - revising (attempt {state['revision_count'] + 1})")
        return "revise"


def build_agent_graph() -> StateGraph:
    """
    Build the LangGraph workflow: Aisha -> Drafter -> Auditor -> (Finish or Revise)
    """
    # Initialize the graph
    workflow = StateGraph(AgentState)
    
    # Add nodes
    workflow.add_node("aisha_intake", aisha_intake_node)
    workflow.add_node("drafter", drafter_node)
    workflow.add_node("auditor", auditor_node)
    
    # Set entry point
    workflow.set_entry_point("aisha_intake")
    
    # Add edges
    workflow.add_edge("aisha_intake", "drafter")
    workflow.add_edge("drafter", "auditor")
    
    # Add conditional edge from auditor
    workflow.add_conditional_edges(
        "auditor",
        should_revise,
        {
            "finish": END,
            "revise": "drafter"
        }
    )
    
    # Compile the graph
    app = workflow.compile()
    
    return app


