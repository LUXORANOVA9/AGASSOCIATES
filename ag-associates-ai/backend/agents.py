# Register pgvector for database operations
register_vector()


class AgentState(TypedDict):
    """
    State schema for the agent workflow
    Tracks all variables through the pipeline
    """
    # Input from user
    raw_input: str
    sender: str
    
    # Extracted by Aisha
    tenant_name: Optional[str]
    landlord_name: Optional[str]
    rent_amount: Optional[str]
    property_address: Optional[str]
    agreement_start_date: Optional[str]
    agreement_duration: Optional[str]
    security_deposit: Optional[str]
    extracted_json: Optional[Dict[str, Any]]
    
    # Generated by Drafter
    template_id: Optional[int]
    template_content: Optional[str]
    drafted_document: Optional[str]
    pdf_path: Optional[str]
    
    # Audit results
    audit_passed: bool
    audit_feedback: Optional[str]
    revision_count: int
    
    # Metadata
    current_node: str
    errors: List[str]
    timestamps: Dict[str, str]


def get_db_connection():
    """Create database connection with pgvector support"""
    conn = psycopg2.connect(
        get_database_url(),
        cursor_factory=RealDictCursor
    )
    return conn


def generate_embedding(text: str) -> List[float]:
    """
    Generate embedding for a given text using local LLM
    In production, use a dedicated embedding model
    """
    # For now, return a dummy embedding - in production use sentence-transformers or similar
    # This is a placeholder that should be replaced with actual embedding generation
    embedding = np.random.rand(EMBEDDING_DIMENSION).astype(np.float32)
    return embedding.tolist()


def similarity_search(query: str, limit: int = 3) -> List[Dict[str, Any]]:
    """
    Perform similarity search in PostgreSQL to find relevant templates
    """
    # Generate embedding for the query
    query_embedding = generate_embedding(query)

    conn = get_db_connection()
    try:
        cur = conn.cursor()
        try:
            # Perform vector similarity search
            cur.execute("""
                SELECT id, title, content, language, jurisdiction,
                       1 - (embedding <=> %s::vector) as similarity
                FROM legal_templates
                ORDER BY embedding <=> %s::vector
                LIMIT %s
            """, (query_embedding, query_embedding, limit))

            results = cur.fetchall()
        finally:
            cur.close()
    finally:
        conn.close()

    return [dict(row) for row in results]


